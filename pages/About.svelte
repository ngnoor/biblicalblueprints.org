<h1>President</h1>

<img src="images/pk2.jpg" class="wraparound">

<p>
	Phillip Kayser has degrees in education, theology, and philosophy, receiving an M.Div from Westminster Theological Seminary and a Ph.D from Whitefield Theological Seminary. Ordained in 1987, he serves as Senior Pastor of Dominion Covenant Church, a Presbyterian (CPC) church in Omaha, Nebraska. He has been privileged to speak at conferences hosted by the National Center For Family-Integrated Churches and Generations.org, and has spoken at three presidential campaign rallies.
</p>
<p>
	Professionally, he serves as Professor of Ethics at Whitefield Theological Seminary, is a board member for the Pickering Foundation of Biblical Preservation, and has lectured at the University of Nebraska at Omaha. He carefully defended Biblical inerrancy in his book, <em>The Canon of Scripture: A Presuppositional Study</em>, and has written over a dozen other books dealing with difficult theological subjects. Many of these works are being translated into foreign languages to equip the worldwide church.
</p>
<p>
	After traveling internationally for several years offering leadership training, worldview classes, and theological lectures, he founded Biblical Blueprints LLC in 2020 to better advance his commitment to take the transformational gospel to the ends of the earth. He and his wife, Kathy, have five married children and fifteen grandchildren. In his spare time he enjoys talking theology with his friends and answering a few of his million emails.
</p>
<p>
	His sermons, commentary, and books are available at <a href="https://biblicalblueprints.com">BiblicalBlueprints.com</a>.
</p>

